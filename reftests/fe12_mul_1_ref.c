typedef double fe12[12];

void fe12_mul_1_ref(fe12 C, const fe12 A, const fe12 B)
{
    // Compute L
    C[ 0] = A[0]*B[0];
    C[ 1] = A[0]*B[1] + A[1]*B[0];
    C[ 2] = A[0]*B[2] + A[1]*B[1] + A[2]*B[0];
    C[ 3] = A[0]*B[3] + A[1]*B[2] + A[2]*B[1] + A[3]*B[0];
    C[ 4] = A[0]*B[4] + A[1]*B[3] + A[2]*B[2] + A[3]*B[1] + A[4]*B[0];
    C[ 5] = A[0]*B[5] + A[1]*B[4] + A[2]*B[3] + A[3]*B[2] + A[4]*B[1] + A[5]*B[0];
    C[ 6] =             A[1]*B[5] + A[2]*B[4] + A[3]*B[3] + A[4]*B[2] + A[5]*B[1];
    C[ 7] =                         A[2]*B[5] + A[3]*B[4] + A[4]*B[3] + A[5]*B[2];
    C[ 8] =                                     A[3]*B[5] + A[4]*B[4] + A[5]*B[3];
    C[ 9] =                                                 A[4]*B[5] + A[5]*B[4];
    C[10] =                                                             A[5]*B[5];
}
